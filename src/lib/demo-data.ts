export function getNarrativeForAgent(agent: AgentMeta) {
  return STORY_LIBRARY[agent.id] ?? '这条人生线还没有准备好。';
}

export function getStoryChunks(story: string) {
  return story
    .split(/(?<=。|！|？)\s*/)
    .map((part) => part.trim())
    .filter(Boolean);
}

function founderReply(question: string) {
  if (/(现金流|钱|收入|工资|房贷)/.test(question)) {
    return '我建议你先把个人安全垫准备到 12 到 18 个月，再谈创业。创业前我以为 6 个月够，后来才发现你不是只养自己，你还要给团队留余地。';
  }

  if (/(最难|最痛|最熬|最崩)/.test(question)) {
    return '最难熬的不是没钱，是你得连续很多天假装自己很稳。2024 年春节前我们账上只够活 47 天，我晚上回家还要装作一切正常，那种孤独比降薪更伤。';
  }

  return '如果你真想追问，我会先让你把最怕失去的东西列出来。很多人嘴上问创业，心里真正怕的是身份掉下去、伴侣不理解、房贷顶不住。';
}

function stayedReply(question: string) {
  if (/(后悔|遗憾|可惜)/.test(question)) {
    return '会怀疑，但不算后悔。我更像是把创业冲动先换成了组织内的更大杠杆，代价是少了一种“彻底为自己冒险”的快感。';
  }

  if (/(怎么判断|什么时候|时机)/.test(question)) {
    return '我会看三件事：你是不是已经讨厌到无法继续、你有没有至少一年安全垫、你到底想要独立决策权还是只是想逃离现在的老板。';
  }

  return '留在大厂不是保守，前提是你真的在用这段时间换筹码，而不是把不动解释成深思熟虑。';
}

function failedReply(question: string) {
  if (/(失败|回头|重来|避免)/.test(question)) {
    return '如果重来，我会把“有人愿不愿意付钱”提到所有愿景前面。别让团队在你的想象力里陪跑 18 个月，这件事我到现在想起来都难受。';
  }

  if (/(回来|回大厂|面子)/.test(question)) {
    return '回大厂第一周我确实觉得丢脸，但那种羞耻感只持续了几天，现金流焦虑和对团队的愧疚反而持续了更久。面子不是最贵的，时间才是。';
  }

  return '创业失败以后，我最大的变化是更尊重低成本验证。你现在如果连第一批付费用户都没影子，就别把辞职当成证明勇气的仪式。';
}

export function getFollowupReply(agentId: string, question: string) {
  switch (agentId) {
    case 'founder-still-running':
      return founderReply(question);
    case 'stayed-in-big-tech':
      return stayedReply(question);
    case 'failed-and-returned':
      return failedReply(question);
    default:
      return '这条人生线暂时还没有准备好追问回复。';
  }
}
